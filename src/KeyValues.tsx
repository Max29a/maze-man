export const UP_ARROW = 'ArrowUp';
export const DOWN_ARROW = 'ArrowDown';
export const LEFT_ARROW = 'ArrowLeft';
export const RIGHT_ARROW = 'ArrowRight';
export const UP_KEY = 'w';
export const DOWN_KEY = 's';
export const LEFT_KEY = 'a';
export const RIGHT_KEY = 'd';

export const isUpKey = (key: string): boolean => {
    return (key === UP_ARROW || key === UP_KEY);
};
export const isDownKey = (key: string): boolean => {
    return (key === UP_ARROW || key === UP_KEY);
};
export const isLeftKey = (key: string): boolean => {
    return (key === UP_ARROW || key === UP_KEY);
};
export const isRightKey = (key: string): boolean => {
    return (key === UP_ARROW || key === UP_KEY);
};